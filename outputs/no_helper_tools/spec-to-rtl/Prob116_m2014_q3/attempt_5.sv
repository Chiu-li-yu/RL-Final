module TopModule (
    input logic [3:0] x,
    output logic f
);
    // Re-reading K-map again.
    // The inputs are x[0], x[1], x[2], x[3]
    // The K-map labels:
    // Rows: x[0]x[1]
    // Cols: x[2]x[3]
    
    // x[0]x[1] \ x[2]x[3] | 00 | 01 | 11 | 10
    // ----------------------------------------
    // 00                  | d  | 0  | d  | d
    // 01                  | 0  | d  | 1  | 0
    // 11                  | 1  | 1  | d  | d
    // 10                  | 1  | 1  | 0  | d

    always @(*) begin
        // Map based on: row=x[0]x[1], col=x[2]x[3]
        // f(x0,x1,x2,x3)
        // 0000 (x0=0,x1=0,x2=0,x3=0): d->0
        // 0001 (x0=0,x1=0,x2=0,x3=1): 0
        // 0011 (x0=0,x1=0,x2=1,x3=1): d->1
        // 0010 (x0=0,x1=0,x2=1,x3=0): d->1
        // 0100 (x0=0,x1=1,x2=0,x3=0): 0
        // 0101 (x0=0,x1=1,x2=0,x3=1): d->1
        // 0111 (x0=0,x1=1,x2=1,x3=1): 1
        // 0110 (x0=0,x1=1,x2=1,x3=0): 0
        // 1100 (x0=1,x1=1,x2=0,x3=0): 1
        // 1101 (x0=1,x1=1,x2=0,x3=1): 1
        // 1111 (x0=1,x1=1,x2=1,x3=1): d->1
        // 1110 (x0=1,x1=1,x2=1,x3=0): d->1
        // 1000 (x0=1,x1=0,x2=0,x3=0): 1
        // 1001 (x0=1,x1=0,x2=0,x3=1): 1
        // 1011 (x0=1,x1=0,x2=1,x3=1): 0
        // 1010 (x0=1,x1=0,x2=1,x3=0): d->1

        case (x)
            4'b0010: f = 1;
            4'b0011: f = 1;
            4'b0101: f = 1;
            4'b0111: f = 1;
            4'b1100: f = 1;
            4'b1101: f = 1;
            4'b1110: f = 1;
            4'b1111: f = 1;
            4'b1000: f = 1;
            4'b1001: f = 1;
            4'b1010: f = 1;
            default: f = 0;
        endcase
    end
endmodule
