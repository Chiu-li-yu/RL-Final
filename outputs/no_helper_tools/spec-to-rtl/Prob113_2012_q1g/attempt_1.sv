module TopModule (
    input [3:0] x,
    output f
);
    // K-map mapping:
    // x[2]x[3] | x[0]x[1]
    //          | 00 | 01 | 11 | 10
    // ---------+----+----+----+----
    // 00       | 1  | 0  | 0  | 1
    // 01       | 0  | 0  | 0  | 0
    // 11       | 1  | 1  | 1  | 0
    // 10       | 1  | 1  | 0  | 1
    
    // minterms where f=1:
    // x[2]x[3]x[0]x[1]
    // 0000 -> 0
    // 0010 -> 2
    // 1100 -> 12
    // 1101 -> 13
    // 1111 -> 15
    // 1000 -> 8
    // 1001 -> 9
    // 1010 -> 10
    
    always @(*) begin
        case (x)
            4'b0000: f = 1'b1;
            4'b0010: f = 1'b1;
            4'b1100: f = 1'b1;
            4'b1101: f = 1'b1;
            4'b1111: f = 1'b1;
            4'b1000: f = 1'b1;
            4'b1001: f = 1'b1;
            4'b1010: f = 1'b1;
            default: f = 1'b0;
        endcase
    end
endmodule
