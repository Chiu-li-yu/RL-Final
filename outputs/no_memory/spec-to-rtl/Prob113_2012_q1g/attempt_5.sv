module TopModule (
    input [3:0] x,
    output f
);
    // x[3] is MSB, x[0] is LSB.
    // Based on the provided K-map:
    // Rows: x[3]x[2], Cols: x[1]x[0]
    // Values:
    // 00 00: 1
    // 00 01: 0
    // 00 11: 0
    // 00 10: 1
    // 01 00: 0
    // 01 01: 0
    // 01 11: 0
    // 01 10: 0
    // 11 00: 1
    // 11 01: 1
    // 11 11: 1
    // 11 10: 0
    // 10 00: 1
    // 10 01: 1
    // 10 11: 0
    // 10 10: 1

    // Min terms:
    // (x3=0, x2=0, x1=0, x0=0) -> ~x3 & ~x2 & ~x1 & ~x0
    // (x3=0, x2=0, x1=1, x0=0) -> ~x3 & ~x2 &  x1 & ~x0
    // (x3=1, x2=1, x1=0, x0=0) ->  x3 &  x2 & ~x1 & ~x0
    // (x3=1, x2=1, x1=0, x0=1) ->  x3 &  x2 & ~x1 &  x0
    // (x3=1, x2=1, x1=1, x0=1) ->  x3 &  x2 &  x1 &  x0
    // (x3=1, x2=0, x1=0, x0=0) ->  x3 & ~x2 & ~x1 & ~x0
    // (x3=1, x2=0, x1=0, x0=1) ->  x3 & ~x2 & ~x1 &  x0
    // (x3=1, x2=0, x1=1, x0=0) ->  x3 & ~x2 &  x1 & ~x0

    assign f = (~x[3] & ~x2 & ~x0) |
               (x[3] & x[2] & ~x1 & ~x0) |
               (x[3] & x[2] & ~x1 & x[0]) |
               (x[3] & x[2] & x[1] & x[0]) |
               (x[3] & ~x[2] & ~x[1] & ~x[0]) |
               (x[3] & ~x[2] & ~x[1] & x[0]) |
               (x[3] & ~x[2] & x[1] & ~x[0]);

endmodule
