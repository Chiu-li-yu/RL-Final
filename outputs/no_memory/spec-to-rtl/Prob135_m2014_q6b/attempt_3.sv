module TopModule (
    input [2:0] y,
    input w,
    output Y1
);
    // State assignments:
    // A: 000, B: 001, C: 010, D: 011, E: 100, F: 101
    // State y[2:0] is (y2, y1, y0)

    // Current State (y2, y1, y0) | w | Next State (y2', y1', y0')
    // -----------------------------------------------------------
    // A: 000 | 0 | B: 001
    // A: 000 | 1 | A: 000
    // B: 001 | 0 | C: 010
    // B: 001 | 1 | D: 011
    // C: 010 | 0 | E: 100
    // C: 010 | 1 | D: 011
    // D: 011 | 0 | F: 101
    // D: 011 | 1 | A: 000
    // E: 100 | 0 | E: 100
    // E: 100 | 1 | D: 011
    // F: 101 | 0 | C: 010
    // F: 101 | 1 | D: 011

    // We need y1' (the next state of y[1])
    // From table:
    // y2 y1 y0 | w | y1'
    // 0  0  0  | 0 | 0 (B: 001)
    // 0  0  0  | 1 | 0 (A: 000)
    // 0  0  1  | 0 | 1 (C: 010)
    // 0  0  1  | 1 | 1 (D: 011)
    // 0  1  0  | 0 | 0 (E: 100)
    // 0  1  0  | 1 | 1 (D: 011)
    // 0  1  1  | 0 | 0 (F: 101)
    // 0  1  1  | 1 | 0 (A: 000)
    // 1  0  0  | 0 | 0 (E: 100)
    // 1  0  0  | 1 | 1 (D: 011)
    // 1  0  1  | 0 | 1 (C: 010)
    // 1  0  1  | 1 | 1 (D: 011)

    // Using K-map for y1':
    // Inputs: y2, y1, y0, w
    // y1' = (~y2 & ~y1 & y0) | (~y2 & y1 & ~y0 & w) | (y2 & ~y1 & w) | (y2 & ~y1 & y0)
    // Simplifying:
    // y1' = (~y2 & ~y1 & y0) | (~y2 & y1 & ~y0 & w) | (y2 & ~y1 & (w | y0))

    assign Y1 = (~y[2] & ~y[1] & y[0]) | (~y[2] & y[1] & ~y[0] & w) | (y[2] & ~y[1] & (w | y[0]));

endmodule
