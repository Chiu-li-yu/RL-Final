module TopModule (
    input [3:0] x,
    output f
);
    // K-map (x3 x2 x1 x0)
    // Row (x3 x2), Col (x1 x0)
    // 00 00: d, 00 01: 0, 00 11: d, 00 10: d
    // 01 00: 0, 01 01: d, 01 11: 1, 01 10: 0
    // 11 00: 1, 11 01: 1, 11 11: d, 11 10: d
    // 10 00: 1, 10 01: 1, 10 11: 0, 10 10: d

    // Assigning d:
    // 0000: 0, 0011: 0, 0010: 0
    // 0101: 0
    // 1111: 0, 1110: 0
    // 1010: 0

    // Truth Table:
    // 0000: 0
    // 0001: 0
    // 0010: 0
    // 0011: 0
    // 0100: 0
    // 0101: 0
    // 0110: 0
    // 0111: 1
    // 1000: 1
    // 1001: 1
    // 1010: 0
    // 1011: 0
    // 1100: 1
    // 1101: 1
    // 1110: 0
    // 1111: 0

    // Karnaugh Map analysis:
    // f = x[3] & ~x[1] | ~x[3] & x[2] & x[1] & x[0]
    // Let's verify:
    // 0111: ~0 & 1 & 1 & 1 = 1 (OK)
    // 1000: 1 & ~0 | ... = 1 (OK)
    // 1001: 1 & ~0 | ... = 1 (OK)
    // 1100: 1 & ~0 | ... = 1 (OK)
    // 1101: 1 & ~0 | ... = 1 (OK)
    // 1110: 1 & ~1 | ... = 0 (OK)
    // 1111: 1 & ~1 | ... = 0 (OK)
    // Others should be 0.

    assign f = (x[3] & ~x[1]) | (~x[3] & x[2] & x[1] & x[0]);
endmodule
