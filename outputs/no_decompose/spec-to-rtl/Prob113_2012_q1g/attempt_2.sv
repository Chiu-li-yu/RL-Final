module TopModule (
    input [3:0] x,
    output f
);
    // Re-mapping the Karnaugh map:
    // x is 4 bits: x[2]x[3]x[0]x[1]
    // The columns are x[0]x[1], rows are x[2]x[3]
    // Indices:
    // row 00 (x[2]x[3]=00): x[0]x[1]=00(0), 01(1), 11(3), 10(2)
    // Values: m(0)=1, m(1)=0, m(3)=0, m(2)=1 -> [0000]=1, [0001]=0, [0011]=0, [0010]=1
    
    // row 01 (x[2]x[3]=01): x[0]x[1]=00(4), 01(5), 11(7), 10(6)
    // Values: m(4)=0, m(5)=0, m(7)=0, m(6)=0 -> [0100]=0, [0101]=0, [0111]=0, [0110]=0

    // row 11 (x[2]x[3]=11): x[0]x[1]=00(12), 01(13), 11(15), 10(14)
    // Values: m(12)=1, m(13)=1, m(15)=1, m(14)=0 -> [1100]=1, [1101]=1, [1111]=1, [1110]=0
    
    // row 10 (x[2]x[3]=10): x[0]x[1]=00(8), 01(9), 11(11), 10(10)
    // Values: m(8)=1, m(9)=1, m(11)=0, m(10)=1 -> [1000]=1, [1001]=1, [1011]=0, [1010]=1

    // Correct mapping based on x = {x[2], x[3], x[0], x[1]}
    // f(x2, x3, x0, x1):
    // 0000: 1
    // 0001: 0
    // 0010: 1
    // 0011: 0
    // 0100: 0
    // 0101: 0
    // 0110: 0
    // 0111: 0
    // 1000: 1
    // 1001: 1
    // 1010: 1
    // 1011: 0
    // 1100: 1
    // 1101: 1
    // 1110: 0
    // 1111: 1

    logic f_val;
    always @(*) begin
        case (x)
            4'b0000: f_val = 1'b1;
            4'b0010: f_val = 1'b1;
            4'b1000: f_val = 1'b1;
            4'b1001: f_val = 1'b1;
            4'b1010: f_val = 1'b1;
            4'b1100: f_val = 1'b1;
            4'b1101: f_val = 1'b1;
            4'b1111: f_val = 1'b1;
            default: f_val = 1'b0;
        endcase
    end
    assign f = f_val;
endmodule
