module TopModule(
    input [3:0] x,
    output f
);
    // x[3] is MSB, x[0] is LSB
    // Map: x3 x2 | x1 x0
    // 00 | d 0 d d  -> 0000:d, 0001:0, 0011:d, 0010:d
    // 01 | 0 d 1 0  -> 0100:0, 0101:d, 0111:1, 0110:0
    // 11 | 1 1 d d  -> 1100:1, 1101:1, 1111:d, 1110:d
    // 10 | 1 1 0 d  -> 1000:1, 1001:1, 1011:0, 1010:d

    // Let's choose d to simplify:
    // 0000:1, 0001:0, 0011:0, 0010:0
    // 0100:0, 0101:0, 0111:1, 0110:0
    // 1100:1, 1101:1, 1111:1, 1110:1
    // 1000:1, 1001:1, 1011:0, 1010:0

    // Karnaugh Map:
    //      x1x0
    // x3x2 00 01 11 10
    // 00 | 1  0  0  0
    // 01 | 0  0  1  0
    // 11 | 1  1  1  1
    // 10 | 1  1  0  0

    // Logic simplification (Sum of Products):
    // 1s at: 0000, 0111, 1100, 1101, 1111, 1110, 1000, 1001
    // f = (~x3 & ~x2 & ~x1 & ~x0) + (x3 & x2) + (x3 & ~x2 & ~x1) + (~x3 & x2 & x1 & x0)

    assign f = (~x[3] & ~x[2] & ~x[1] & ~x[0]) | 
               (x[3] & x[2]) | 
               (x[3] & ~x[2] & ~x[1]) | 
               (~x[3] & x[2] & x[1] & x[0]);
endmodule
