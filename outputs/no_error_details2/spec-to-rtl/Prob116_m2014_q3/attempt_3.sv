module TopModule(
    input [3:0] x,
    output f
);
    // K-map:
    // Row/Col   x1x0: 00 01 11 10
    // x3x2:
    // 00 | d 0 d d -> 00
    // 01 | 0 d 1 0 -> 0111 is 1
    // 11 | 1 1 d d -> 1100, 1101 are 1
    // 10 | 1 1 0 d -> 1000, 1001 are 1

    // 1s: (0,1,1) -> 0111
    // 1s: (1,1,0,0) -> 1100
    // 1s: (1,1,0,1) -> 1101
    // 1s: (1,0,0,0) -> 1000
    // 1s: (1,0,0,1) -> 1001

    // Mapping x to x[3]x[2]x[1]x[0]
    // Truth table:
    // x3x2 x1x0 | f
    // 00 00 | ?
    // 00 01 | 0
    // 00 10 | ?
    // 00 11 | ?
    // 01 00 | 0
    // 01 01 | ?
    // 01 10 | 0
    // 01 11 | 1
    // 10 00 | 1
    // 10 01 | 1
    // 10 10 | ?
    // 10 11 | 0
    // 11 00 | 1
    // 11 01 | 1
    // 11 10 | ?
    // 11 11 | ?

    // f = (x3 & ~x2 & ~x1) | (x3 & x2 & ~x1) | (x2 & x1 & x0)
    // f = (x3 & ~x1) | (x2 & x1 & x0)

    assign f = (x[3] & ~x[1]) | (x[2] & x[1] & x[0]);
endmodule
