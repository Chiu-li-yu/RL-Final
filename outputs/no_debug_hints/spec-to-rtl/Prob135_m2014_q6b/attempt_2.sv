module TopModule (
    input [2:0] y,
    input w,
    output Y1
);

    // Current State: y[2:0]
    // Next State Logic: ny[2:0]
    //
    // A: 000, B: 001, C: 010, D: 011, E: 100, F: 101
    //
    // Transition | w | Next | ny[1]
    // -----------|---|------|------
    // 000 (A)    | 0 | 001  | 0
    // 000 (A)    | 1 | 000  | 0
    // 001 (B)    | 0 | 010  | 1
    // 001 (B)    | 1 | 011  | 1
    // 010 (C)    | 0 | 100  | 0
    // 010 (C)    | 1 | 011  | 1
    // 011 (D)    | 0 | 101  | 0
    // 011 (D)    | 1 | 000  | 0
    // 100 (E)    | 0 | 100  | 0
    // 100 (E)    | 1 | 011  | 1
    // 101 (F)    | 0 | 010  | 1
    // 101 (F)    | 1 | 011  | 1
    
    // ny[1] = 1 when:
    // (y=001, w=0) -> 001 & ~w
    // (y=001, w=1) -> 001 & w
    // (y=010, w=1) -> 010 & w
    // (y=100, w=1) -> 100 & w
    // (y=101, w=0) -> 101 & ~w
    // (y=101, w=1) -> 101 & w
    
    // Simplification:
    // y=001: (001&~w) | (001&w) = 001
    // y=010: (010&w)
    // y=100: (100&w)
    // y=101: (101&~w) | (101&w) = 101
    
    // ny[1] = (y==3'b001) | (y==3'b010 && w) | (y==3'b100 && w) | (y==3'b101)
    // ny[1] = (y == 3'b001) | (y == 3'b101) | (w & (y == 3'b010)) | (w & (y == 3'b100))

    assign Y1 = (y == 3'b001) | (y == 3'b101) | (w & (y == 3'b010)) | (w & (y == 3'b100));

endmodule
