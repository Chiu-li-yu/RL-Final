module TopModule(
    input [3:0] x,
    output f
);
    // Let's re-map based on the provided K-map description strictly.
    // Row: x[3]x[4] -> x[3]x[2]
    // Col: x[1]x[2] -> x[1]x[0]
    
    // x = [x3, x2, x1, x0]
    // row = {x[3], x[2]}
    // col = {x[1], x[0]}
    
    // Map:
    //      00 01 11 10 (col)
    // 00 | d  0  d  d
    // 01 | 0  d  1  0
    // 11 | 1  1  d  d
    // 10 | 1  1  0  d
    
    // f(x3, x2, x1, x0):
    // 0000: d(0), 0001:0, 0011:d(0), 0010:d(0)
    // 0100: 0, 0101:d(1), 0111:1, 0110:0
    // 1100: 1, 1101:1, 1111:d(0), 1110:d(0)
    // 1000: 1, 1001:1, 1011:0, 1010:d(0)
    
    // Let's fill d to simplify:
    // 0000: 0, 0010: 0, 0011: 0
    // 0101: 1
    // 1110: 0, 1111: 0
    // 1010: 0
    
    // K-map table (x3 x2 x1 x0):
    // 0000 0
    // 0001 0
    // 0010 0
    // 0011 0
    // 0100 0
    // 0101 1
    // 0110 0
    // 0111 1
    // 1000 1
    // 1001 1
    // 1010 0
    // 1011 0
    // 1100 1
    // 1101 1
    // 1110 0
    // 1111 0
    
    // Map:
    // Row 00: 0 0 0 0
    // Row 01: 0 1 1 0
    // Row 11: 1 1 0 0
    // Row 10: 1 1 0 0
    
    // f = (~x3 & x2 & x0) | (~x3 & x2 & x1) | (x3 & ~x1)
    //   = (~x3 & x2 & (x0 | x1)) | (x3 & ~x1)
    
    assign f = (~x[3] & x[2] & (x[1] | x[0])) | (x[3] & ~x[1]);
endmodule
