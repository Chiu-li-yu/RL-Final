
module TopModule (
    input [3:0] x,
    output logic f
);

    always @(*) begin
        case (x)
            // Mapping: x[3]=x4, x[2]=x3, x[1]=x2, x[0]=x1
            // K-map indices: Row (x3,x4) Col (x1,x2)
            // x = {x4, x3, x2, x1} -> {x[3], x[2], x[1], x[0]}
            
            // Row 00: x[3]=0, x[2]=0
            4'b0000: f = 1'b0; // x[3]=0, x[2]=0, x[1]=0, x[0]=0 -> d
            4'b0001: f = 1'b0; // x[3]=0, x[2]=0, x[1]=0, x[0]=1 -> 0
            4'b0011: f = 1'b0; // x[3]=0, x[2]=0, x[1]=1, x[0]=1 -> d
            4'b0010: f = 1'b0; // x[3]=0, x[2]=0, x[1]=1, x[0]=0 -> d
            
            // Row 01: x[3]=0, x[2]=1 (Wait, x[3]x[4] = 01 means x[2]=0, x[3]=1)
            // Let's re-evaluate mapping: 
            // x[3] is the first bit of the vertical pair, x[4] is the second.
            // Row 00: x[3]=0, x[4]=0  => x[2]=0, x[3]=0
            // Row 01: x[3]=0, x[4]=1  => x[2]=0, x[3]=1
            // Row 11: x[3]=1, x[4]=1  => x[2]=1, x[3]=1
            // Row 10: x[3]=1, x[4]=0  => x[2]=1, x[3]=0
            
            // Column 00: x[1]=0, x[2]=0 => x[0]=0, x[1]=0
            // Column 01: x[1]=0, x[2]=1 => x[0]=0, x[1]=1
            // Column 11: x[1]=1, x[2]=1 => x[0]=1, x[1]=1
            // Column 10: x[1]=1, x[2]=0 => x[0]=1, x[1]=0
            
            // Recalculating case values (x[3]x[2]x[1]x[0]):
            // Row 00, Col 00: 0000 -> d
            // Row 00, Col 01: 0010 -> 0 (x[3]=0, x[2]=0, x[1]=1, x[0]=0) - wait, x[1]=0, x[2]=1
            // Let's be extremely careful.
            // Prompt: x[1]x[2] (Cols), x[3]x[4] (Rows)
            // Col 00: x1=0, x2=0
            // Col 01: x1=0, x2=1
            // Col 11: x1=1, x2=1
            // Col 10: x1=1, x2=0
            // Row 00: x3=0, x4=0
            // Row 01: x3=0, x4=1
            // Row 11: x3=1, x4=1
            // Row 10: x3=1, x4=0
            
            // If x = [3:0], let x[0]=x1, x[1]=x2, x[2]=x3, x[3]=x4
            // Row 00 (x[2]=0, x[3]=0), Col 00 (x[0]=0, x[1]=0) => 0000 : d
            // Row 00 (x[2]=0, x[3]=0), Col 01 (x[0]=0, x[1]=1) => 0010 : 0
            // Row 00 (x[2]=0, x[3]=0), Col 11 (x[0]=1, x[1]=1) => 0011 : d (wait, x[0]=1, x[1]=1 is 0011)
            // Row 00 (x[2]=0, x[3]=0), Col 10 (x[0]=1, x[1]=0) => 0001 : d
            
            // Row 01 (x[2]=0, x[3]=1), Col 00 (x[0]=0, x[1]=0) => 1000 : 0 (x[3]=1, x[2]=0, x[1]=0, x[0]=0)
            // Row 01 (x[2]=0, x[3]=1), Col 01 (x[0]=0, x[1]=1) => 1010 : d
            // Row 01 (x[2]=0, x[3]=1), Col 11 (x[0]=1, x[1]=1) => 1011 : 1
            // Row 01 (x[2]=0, x[3]=1), Col 10 (x[0]=1, x[1]=0) => 1001 : 0
            
            // Row 11 (x[2]=1, x[3]=1), Col 00 (x[0]=0, x[1]=0) => 1100 : 1
            // Row 11 (x[2]=1, x[3]=1), Col 01 (x[0]=0, x[1]=1) => 1110 : 1
            // Row 11 (x[2]=1, x[3]=1), Col 11 (x[0]=1, x[1]=1) => 1111 : d
            // Row 11 (x[2]=1, x[3]=1), Col 10 (x[0]=1, x[1]=0) => 1101 : d
            
            // Row 10 (x[2]=1, x[3]=0), Col 00 (x[0]=0, x[1]=0) => 0100 : 1
            // Row 10 (x[2]=1, x[3]=0), Col 01 (x[0]=0, x[1]=1) => 0110 : 1
            // Row 10 (x[2]=1, x[3]=0), Col 11 (x[0]=1, x[1]=1) => 0111 : 0
            // Row 10 (x[2]=1, x[3]=0), Col 10 (x[0]=1, x[1]=0) => 0101 : d
            
            default: f = 1'b0;
        endcase
    end
endmodule
