module TopModule (
    input [3:0] x,
    output f
);
    // Based on K-map:
    // x[3]x[2] (rows) | x[1]x[0] (cols)
    // 00: d, 0, d, d  -> 0000:x, 0001:0, 0011:x, 0010:x
    // 01: 0, d, 1, 0  -> 0100:0, 0101:x, 0111:1, 0110:0
    // 11: 1, 1, d, d  -> 1100:1, 1101:1, 1111:x, 1110:x
    // 10: 1, 1, 0, d  -> 1000:1, 1001:1, 1011:0, 1010:x
    
    // Choosing d=0:
    // 0000:0, 0001:0, 0010:0, 0011:0
    // 0100:0, 0101:0, 0111:1, 0110:0
    // 1100:1, 1101:1, 1110:0, 1111:0
    // 1000:1, 1001:1, 1010:0, 1011:0
    
    // Logic extraction:
    // f = 1 when:
    // 0111 (x3=0, x2=1, x1=1, x0=1)
    // 1100 (x3=1, x2=1, x1=0, x0=0)
    // 1101 (x3=1, x2=1, x1=0, x0=1)
    // 1000 (x3=1, x2=0, x1=0, x0=0)
    // 1001 (x3=1, x2=0, x1=0, x0=1)
    
    assign f = (~x[3] & x[2] & x[1] & x[0]) | 
               (x[3] & x[2] & ~x[1]) | 
               (x[3] & ~x[2] & ~x[1]);

endmodule