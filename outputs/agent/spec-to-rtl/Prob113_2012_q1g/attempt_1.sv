
module TopModule (
    input [3:0] x,
    output logic f
);

    always @(*) begin
        case (x)
            4'b0000: f = 1'b1; // R00 C00: x[2]=0,x[3]=0, x[0]=0,x[1]=0
            4'b0001: f = 1'b1; // R00 C10: x[2]=0,x[3]=0, x[0]=1,x[1]=0
            4'b0010: f = 1'b0; // R00 C01: x[2]=0,x[3]=0, x[0]=0,x[1]=1
            4'b0011: f = 1'b0; // R00 C11: x[2]=0,x[3]=0, x[0]=1,x[1]=1
            4'b0100: f = 1'b1; // R10 C00: x[2]=1,x[3]=0, x[0]=0,x[1]=0
            4'b0101: f = 1'b1; // R10 C10: x[2]=1,x[3]=0, x[0]=1,x[1]=0
            4'b0110: f = 1'b1; // R10 C01: x[2]=1,x[3]=0, x[0]=0,x[1]=1
            4'b0111: f = 1'b0; // R10 C11: x[2]=1,x[3]=0, x[0]=1,x[1]=1
            4'b1000: f = 1'b0; // R01 C00: x[2]=0,x[3]=1, x[0]=0,x[1]=0
            4'b1001: f = 1'b0; // R01 C10: x[2]=0,x[3]=1, x[0]=1,x[1]=0
            4'b1010: f = 1'b0; // R01 C01: x[2]=0,x[3]=1, x[0]=0,x[1]=1
            4'b1011: f = 1'b0; // R01 C11: x[2]=0,x[3]=1, x[0]=1,x[1]=1
            4'b1100: f = 1'b1; // R11 C00: x[2]=1,x[3]=1, x[0]=0,x[1]=0
            4'b1101: f = 1'b0; // R11 C10: x[2]=1,x[3]=1, x[0]=1,x[1]=0
            4'b1110: f = 1'b1; // R11 C01: x[2]=1,x[3]=1, x[0]=0,x[1]=1
            4'b1111: f = 1'b1; // R11 C11: x[2]=1,x[3]=1, x[0]=1,x[1]=1
            default: f = 1'b0;
        endcase
    end

endmodule
