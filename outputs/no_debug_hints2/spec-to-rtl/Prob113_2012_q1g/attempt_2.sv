module TopModule (
    input [3:0] x,
    output f
);
    // Inputs: x[3], x[2], x[1], x[0]
    // Mapping:
    // x = {x[3], x[2], x[1], x[0]}
    // Let's re-verify:
    // K-map (Row: x[2]x[3], Col: x[0]x[1]):
    // (x2, x3, x0, x1) -> f
    // 0,0, 0,0 (x=0000) -> 1
    // 0,0, 1,0 (x=0010) -> 1
    // 0,0, 0,1 (x=0001) -> 0
    // 0,0, 1,1 (x=0011) -> 0
    // 0,1, 0,0 (x=0100) -> 0
    // 0,1, 1,0 (x=0110) -> 0
    // 0,1, 0,1 (x=0101) -> 0
    // 0,1, 1,1 (x=0111) -> 0
    // 1,1, 0,0 (x=1100) -> 1
    // 1,1, 0,1 (x=1101) -> 1
    // 1,1, 1,1 (x=1111) -> 1
    // 1,1, 1,0 (x=1110) -> 0
    // 1,0, 0,0 (x=1000) -> 1
    // 1,0, 0,1 (x=1001) -> 1
    // 1,0, 1,0 (x=1010) -> 1
    // 1,0, 1,1 (x=1011) -> 0

    // Using the truth table, f is 1 for inputs:
    // 0000, 0010, 1100, 1101, 1111, 1000, 1001, 1010
    
    // In decimal: 0, 2, 12, 13, 15, 8, 9, 10
    
    assign f = (x == 0) || (x == 2) || (x == 12) || (x == 13) || (x == 15) || (x == 8) || (x == 9) || (x == 10);
endmodule
