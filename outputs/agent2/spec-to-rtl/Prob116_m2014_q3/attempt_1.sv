module TopModule (
    input [3:0] x,
    output f
);
    // x[3]x[4] as row, x[1]x[2] as column
    // Mapping:
    // 0000 (0): d -> 0
    // 0001 (1): 0
    // 0011 (3): d -> 0
    // 0010 (2): d -> 0
    // 0100 (4): 0
    // 0101 (5): d -> 0
    // 0111 (7): 1
    // 0110 (6): 0
    // 1100 (12): 1
    // 1101 (13): 1
    // 1111 (15): d -> 0
    // 1110 (14): d -> 0
    // 1000 (8): 1
    // 1001 (9): 1
    // 1011 (11): 0
    // 1010 (10): d -> 0

    // Logic:
    // f = 1 when (x[3]=0, x[4]=1, x[1]=1, x[2]=1) -> 0111 (7)
    // f = 1 when (x[3]=1, x[4]=1, x[1]=0, x[2]=0) -> 1100 (12)
    // f = 1 when (x[3]=1, x[4]=1, x[1]=0, x[2]=1) -> 1101 (13)
    // f = 1 when (x[3]=1, x[4]=0, x[1]=0, x[2]=0) -> 1000 (8)
    // f = 1 when (x[3]=1, x[4]=0, x[1]=0, x[2]=1) -> 1001 (9)

    // Using K-Map simplification:
    // f = (~x[3] & x[4] & x[1] & x[2]) | (x[3] & ~x[1] & ~x[2]) | (x[3] & x[4] & ~x[1])
    // Simplified:
    // f = (~x[3] & x[4] & x[1] & x[2]) | (x[3] & ~x[1])

    assign f = (~x[3] & x[4] & x[1] & x[2]) | (x[3] & ~x[1]);

endmodule
