module TopModule (
    input [3:0] x,
    output f
);
    // K-map (x[2]x[3] rows, x[0]x[1] cols)
    // x: x[3] x[2] x[1] x[0]
    // 
    // x[2]x[3] | 00(0,0) | 01(0,1) | 11(1,1) | 10(1,0) | (x[0]x[1])
    // --------------------------------------------------
    // 00       | 1       | 0       | 0       | 1       |
    // 01       | 0       | 0       | 0       | 0       |
    // 11       | 1       | 1       | 1       | 0       |
    // 10       | 1       | 1       | 0       | 1       |
    
    // Convert to index:
    // x[2]x[3] x[0]x[1] -> x[3]x[2]x[1]x[0]
    // 00 00 -> 0000 (0) : 1
    // 00 01 -> 0100 (4) : 0
    // 00 11 -> 1100 (12): 0
    // 00 10 -> 1000 (8) : 1
    // 01 00 -> 0010 (2) : 0
    // 01 01 -> 0110 (6) : 0
    // 01 11 -> 1110 (14): 0
    // 01 10 -> 1010 (10): 0
    // 11 00 -> 0011 (3) : 1
    // 11 01 -> 0111 (7) : 1
    // 11 11 -> 1111 (15): 1
    // 11 10 -> 1011 (11): 0
    // 10 00 -> 0001 (1) : 1
    // 10 01 -> 0101 (5) : 1
    // 10 11 -> 1101 (13): 0
    // 10 10 -> 1001 (9) : 1

    // Indices where f=1: 0, 1, 3, 5, 7, 8, 9, 15
    // Map these to x[3]x[2]x[1]x[0]:
    // 0: 0000
    // 1: 0001
    // 3: 0011
    // 5: 0101
    // 7: 0111
    // 8: 1000
    // 9: 1001
    // 15: 1111

    logic f_val;
    always @(*) begin
        case (x)
            4'd0, 4'd1, 4'd3, 4'd5, 4'd7, 4'd8, 4'd9, 4'd15: f_val = 1'b1;
            default: f_val = 1'b0;
        endcase
    end
    assign f = f_val;

endmodule
