module TopModule (
    input [3:0] x,
    output f
);
    // x[3]x[2] (rows), x[1]x[0] (cols)
    // 00 01 11 10
    // 00: d, 0, d, d  (0, 1, 3, 2)
    // 01: 0, d, 1, 0  (4, 5, 7, 6)
    // 11: 1, 1, d, d  (12, 13, 15, 14)
    // 10: 1, 1, 0, d  (8, 9, 11, 10)

    // 1s at: 7, 8, 9, 12, 13
    // 0s at: 1, 4, 6, 11
    // d at:  0, 2, 3, 5, 10, 14, 15
    
    // Simplification:
    // f(x3,x2,x1,x0) = sum(7, 8, 9, 12, 13)
    // 12(1100), 13(1101), 8(1000), 9(1001) -> x3 & ~x1
    // 12(1100), 13(1101) -> covered by x3 & ~x1
    // 7(0111) -> ?
    // 8, 9, 12, 13 cover the 1s at 8,9,12,13.
    // 7(0111) is x3' x2 x1 x0.
    
    // Boolean:
    // f = (x[3] & ~x[1]) | (~x[3] & x[2] & x[1] & x[0])
    
    assign f = (x[3] & ~x[1]) | (~x[3] & x[2] & x[1] & x[0]);
endmodule
