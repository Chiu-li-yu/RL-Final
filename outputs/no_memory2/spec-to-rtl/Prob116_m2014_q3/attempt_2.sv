module TopModule (
    input [3:0] x,
    output f
);
    // Based on K-map:
    // x[3]x[2] \ x[1]x[0] | 00 | 01 | 11 | 10 |
    // --------------------|----|----|----|----|
    // 00                  | d  | 0  | d  | d  |
    // 01                  | 0  | d  | 1  | 0  |
    // 11                  | 1  | 1  | d  | d  |
    // 10                  | 1  | 1  | 0  | d  |
    
    // Choosing d=0:
    // f = 1 at:
    // 0111: x3=0, x2=1, x1=1, x0=1 -> x=0111 (wait, map is 01, 11)
    // Let's re-map carefully:
    // Row 00 (0,0): d,0,d,d -> 0,0,0,0
    // Row 01 (0,1): 0,d,1,0 -> 0,0,1,0
    // Row 11 (1,1): 1,1,d,d -> 1,1,0,0
    // Row 10 (1,0): 1,1,0,d -> 1,1,0,0
    
    // f = 1 when (x3,x2,x1,x0) is:
    // 0111 (Row 01, Col 11)
    // 1100 (Row 11, Col 00)
    // 1101 (Row 11, Col 01)
    // 1000 (Row 10, Col 00)
    // 1001 (Row 10, Col 01)

    // Simplified expression (using K-map SOP):
    // f = (~x[3]&x[2]&x[1]&x[0]) | (x[3]&x[2]&~x[1]&~x[0]) | (x[3]&x[2]&~x[1]&x[0]) | (x[3]&~x[2]&~x[1]&~x[0]) | (x[3]&~x[2]&~x[1]&x[0])
    // f = (~x3*x2*x1*x0) + (x3*x2*~x1) + (x3*~x2*~x1)
    // f = (~x[3]&x[2]&x[1]&x[0]) | (x[3]&x[2]&~x[1]) | (x[3]&~x[2]&~x[1])
    // f = (~x[3]&x[2]&x[1]&x[0]) | (x[3]&~x[1])

    assign f = (~x[3] & x[2] & x[1] & x[0]) | (x[3] & ~x[1]);

endmodule
