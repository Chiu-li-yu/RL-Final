module TopModule(
    input a,
    input b,
    input c,
    input d,
    output q
);

    // 根據真值表進行分析：
    // q = 1 當 (a,b,c,d) 為:
    // 0101, 0110, 0111
    // 1001, 1010, 1011
    // 1101, 1110, 1111
    
    // 觀察 q=1 的情況：
    // d 必須為 1，除了 (0110, 0111, 1010, 1011, 1110, 1111) 這些有 d=0 或 d=1 的情況外？
    // 等一下，重新列出 q=1 的輸入：
    // (a,b,c,d)
    // 0101, 0110, 0111
    // 1001, 1010, 1011
    // 1101, 1110, 1111
    
    // 整理：
    // 如果 b=1 且 c=0 且 d=1 -> q=1
    // 如果 b=1 且 c=1 -> q=1 (包含 0110, 0111, 1110, 1111)
    // 如果 a=1 且 b=0 且 d=1 -> q=1
    // 如果 a=1 且 b=0 且 c=1 -> q=1 (包含 1010, 1011)
    // 如果 a=1 且 b=1 且 d=1 -> q=1
    // 如果 a=1 且 b=1 且 c=1 -> q=1 (包含 1110, 1111)

    // 卡諾圖簡化：
    // q = (b & d) | (c & d) | (b & c) | (a & d) | (a & c)
    // 經檢查：
    // q = (b | a) & (c | d) ... 測試一下
    // (0,1,0,1) -> (1) & (1) = 1 (正確)
    // (0,1,1,0) -> (1) & (1) = 1 (正確)
    // (1,0,0,1) -> (1) & (1) = 1 (正確)
    // (0,0,1,1) -> (0) & (1) = 0 (正確)
    
    assign q = (a | b) & (c | d);

endmodule
